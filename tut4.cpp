 #include<iostream>
using namespace std;

 int main()
{
    int num1,num2;
    cout<<"enter the value of num1:\n";/* << this is called as the insertion operator*/
    cin>>num1;/* '>>' called the extraction operator */
    cout<<"enter the value of num2:\n";/* << this is called as the insertion operator*/
    cin>>num2;/* '>>' called the extraction operator */
    cout<<"the sum is"<<num1+num2;
    return 0;
    
}
